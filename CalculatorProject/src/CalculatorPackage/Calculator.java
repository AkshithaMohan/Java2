package CalculatorPackage;

public class Calculator {

	public int add(int a, int b)
	{
		int result = a+b;
		return result;
	}
	public int Sub(int a, int b)
	{
		int result = a-b;
		return result;
	}
	public int Div(int a, int b)
	{
		int result = a/b;
		return result;
	}
	public int Multiply(int a, int b)
	{
		int result = a*b;
		return result;
	}
}
